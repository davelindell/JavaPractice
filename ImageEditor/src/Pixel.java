
public class Pixel {
	private int red;
	private int blue;
	private int green;
	
	public Pixel() {
		red = 0;
		blue = 0;
		green = 0;
	}
	
	public int getRed(){
		return red;
	}
	public int getGreen(){
		return green;
	}
	public int getBlue(){
		return blue;
	}
	
	public void setRed(int value){
		red = value;
	}
	public void setGreen(int value){
		green = value;
	}
	public void setBlue(int value){
		blue = value;
	}
	
}
